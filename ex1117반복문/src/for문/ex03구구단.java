package for문;

public class ex03구구단 {

	public static void main(String[] args) {
		
		// for문을 사용해서 구구단 2단을 출력해보자.
		// 2 * 1 = 2
		// 2 * 2 = 4
		// 2 * 3 = 6
		// 2 * 4 = 8
		// 2 * 5 = 10
		
		// 출력문으로 표현하기
		for(int i = 1; i <= 9; i++) {
			System.out.println("2 * "+ i + "="+2*i);
			
		}

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
