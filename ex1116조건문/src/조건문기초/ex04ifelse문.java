package 조건문기초;

public class ex04ifelse문 {

	public static void main(String[] args) {
		
		int age = 19;
		
		// age가 20보다 크거나 같다면 성인입니다를 출력
		if(age >= 20) {
			// 조건식이 참이었을 때 실행하는 구간
			System.out.println("성인입니다.");
		}else {
			// 그렇지 않은 경우 성인이 아닙니다를 출력
			// 코드 이동 단축키 : alt 화살표 위 or 아래
			// 조건식이 거짓이었을 때 실행하는 구간
			System.out.println("성인이 아닙니다.");
			
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
